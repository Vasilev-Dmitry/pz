# Вариант 8.
# Дан список размера N. Найти количество участков, на которых его элементы
# монотонно возрастают.

N = int(input("Введите размер списка N: "))

arr = []
for i in range(N):
    arr.append(int(input(f"Введите элемент {i}: ")))

count = 0          # количество возрастающих участков
in_segment = False # флаг: находимся ли мы внутри возрастающего участка

for i in range(1, N):
    if arr[i] > arr[i - 1]:
        # продолжается возрастание
        if not in_segment:
            count += 1      # нашли новый участок
            in_segment = True
    else:
        # возрастание прервалось
        in_segment = False

print("Количество возрастающих участков:", count)
