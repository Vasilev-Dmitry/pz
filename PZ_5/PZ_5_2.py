# Вариант 8.
# Описать функцию AddLeftDigit(D, K), добавляющую к целому положительному
# числу K слева цифру D (D — входной параметр целого типа, лежащий в диапазоне
# 1-9, K — параметр целого типа, являющийся одновременно входным и выходным).
# С помощью этой функции последовательно добавить к данному числу K слева
# данные цифры D1 и D2, выводя результат каждого добавления.

def AddLeftDigit(D, K):
    # Превращаем число K в строку, добавляем слева цифру D
    K = int(str(D) + str(K))
    return K

# Ввод исходных данных
K = int(input("Введите число K: "))
D1 = int(input("Введите цифру D1 (1–9): "))
D2 = int(input("Введите цифру D2 (1–9): "))

while type(K) != int:
    try:
        K = int(K)
    except ValueError:
        print("Неправильно ввели!")
        K = input("Введите число K: ")

while type(D1) != int:
    try:
        D1 = int(D1)
    except ValueError:
        print("Неправильно ввели!")
        D1 = input("Введите цифру D1 (1–9): ")

while type(D2) != int:
    try:
        D2 = int(D2)
    except ValueError:
        print("Неправильно ввели!")
        D2 = input("Введите цифру D2 (1–9): ")

# Добавление первой цифры
K = AddLeftDigit(D1, K)
print("После добавления D1:", K)

# Добавление второй цифры
K = AddLeftDigit(D2, K)
print("После добавления D2:", K)