# Вариант 8.
# Начальный вклад: 1000 руб.
# Каждый месяц вклад увеличивается на P процентов.
# Найти: через сколько месяцев вклад превысит 1100 руб.,
# и вывести количество месяцев K и итоговую сумму S.

P = float(input("Введите процент увеличения вклада (0 < P < 25): "))

S = 1000        # текущая сумма
K = 0           # количество месяцев

while S <= 1100:
    S = S + S * (P / 100)   # увеличение на P процентов
    K += 1

print("Количество месяцев:", K)
print("Итоговый вклад:", S)